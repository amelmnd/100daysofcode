#include <unistd.h>
#include <stdio.h>

int main(int c, char *v[])
{
	if (c < 2)
		write(1, "\n", 1);
	else
	{
		int i = 0;
		while (v[1][i])
		{
			int y = 0;
			if (v[1][i] > 64 && v[1][i] <= 90)
			{
				while (y < (int)(v[1][i] - 64))
				{
					write(1, &v[1][i], 1);
					y++;
				}
			}
			else if (v[1][i] >= 97 && v[1][i] <= 122)
			{
				while (y < (int)(v[1][i] - 96))
				{
					write(1, &v[1][i], 1);
					y++;
				}
			}
			i++;
		}
		write(1, "\n", 1);
	}
	return 0;
}
